const FALLBACK_HEX = "1f4d0";

/** Icon khác nhau cho từ chưa có trong bảng (tránh 4 ô cùng hình tam giác). */
const FALLBACK_POOL = [
  "1f4d0", "1f4da", "1f4dd", "1f9ed", "1f9f0", "1f9e9", "1f9fc", "1f9fb",
  "1f3af", "1f3b2", "1f3a8", "1f9e8", "1f9f6", "1f9f5", "1f9f4", "1f9f3",
  "1f9e7", "1f9e6", "1f9e5", "1f9e4", "1f9e3", "1f9e2", "1f9e1", "1f9e0",
];

function hashWordHex(word) {
  const key = String(word || "").toLowerCase().trim();
  let h = 0;
  for (let i = 0; i < key.length; i += 1) h = (h * 31 + key.charCodeAt(i)) | 0;
  return FALLBACK_POOL[Math.abs(h) % FALLBACK_POOL.length];
}

function stems(word) {
  const w = word.toLowerCase();
  const out = new Set([w]);
  if (w.endsWith("ies")) out.add(`${w.slice(0, -3)}y`);
  if (w.endsWith("ing") && w.length > 4) out.add(w.slice(0, -3));
  if (w.endsWith("ed") && w.length > 3) out.add(w.slice(0, -2));
  if (w.endsWith("es") && w.length > 3) out.add(w.slice(0, -2));
  if (w.endsWith("s") && w.length > 2) out.add(w.slice(0, -1));
  return [...out];
}

/**
 * @param {string} word
 * @returns {string} hex không có prefix U+
 */
export function resolveWordEmojiHex(word) {
  const key = String(word || "")
    .toLowerCase()
    .trim()
    .replace(/\s+/g, " ");
  if (!key) return FALLBACK_HEX;

  if (WORD_TO_TWEMOJI_HEX[key]) return WORD_TO_TWEMOJI_HEX[key];
  if (storyWordEmoji[key]) return storyWordEmoji[key];

  for (const s of stems(key)) {
    if (WORD_TO_TWEMOJI_HEX[s]) return WORD_TO_TWEMOJI_HEX[s];
    if (storyWordEmoji[s]) return storyWordEmoji[s];
  }

  for (const [re, hex] of EMOJI_RULES) {
    if (re.test(key)) return hex;
  }
  for (const s of stems(key)) {
    for (const [re, hex] of EMOJI_RULES) {
      if (re.test(s)) return hex;
    }
  }

  return hashWordHex(key);
}
