function normalizeWordKey(word) {
  return String(word || "")
    .toLowerCase()
    .trim()
    .replace(/[^a-z]/g, "");
}

export function getMatchingImageByWord(word) {
  const key = normalizeWordKey(word);
  if (!key) return "";
  if (MATCHING_IMAGE_MAP[key]) return MATCHING_IMAGE_MAP[key];
  if (key.endsWith("ies") && MATCHING_IMAGE_MAP[`${key.slice(0, -3)}y`]) {
    return MATCHING_IMAGE_MAP[`${key.slice(0, -3)}y`];
  }
  if (key.endsWith("es") && MATCHING_IMAGE_MAP[key.slice(0, -2)]) {
    return MATCHING_IMAGE_MAP[key.slice(0, -2)];
  }
  if (key.endsWith("s") && MATCHING_IMAGE_MAP[key.slice(0, -1)]) {
    return MATCHING_IMAGE_MAP[key.slice(0, -1)];
  }
  return "";
}
